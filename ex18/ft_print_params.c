void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int arg;
	int	i;

	arg = 1;
	while (arg < argc)
	{
		i = 0;
		while (argv[arg][i] != '\0')
		{
			ft_putchar(argv[arg][i]);
			i++;
		}
		i = 0;
		ft_putchar('\n');
		arg++;
	}
	return (0);
}
